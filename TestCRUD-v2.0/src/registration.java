    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // Inputs the data into the table
        String user = username.getText();
        String pass2 = confirmpass.getText();
        String status = "";

        //validation for textfields
        if(user.equals("")){
            jOptionPane1.showMessageDialog(null, "Username cannot be empty.");
        }else if(pass2.equals("")){
            jOptionPane1.showMessageDialog(null, "Password cannot be empty.");
        }

        //selector for what is the user's status
        if(admin.isSelected()){
            status = admin.getText();
        }else if(employee.isSelected()){
            status = employee.getText();
        }

        //code for inputing into table
        try{
            conn=DriverManager.getConnection(DB_URL, USER, PASS);
            stmt = conn.createStatement();

            String sql = "INSERT INTO account_table(username, password, status) "
            + "VALUES('"+user+"', '"+pass2+"', '"+status+"')";
            stmt.executeUpdate(sql);

            jOptionPane1.showMessageDialog(null, "User successfully added.");
            conn.close();

        }catch(SQLException se){
            System.out.println(se);
        }
    }//GEN-LAST:event_registerActionPerformed

